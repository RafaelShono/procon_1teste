    private void jListaAtendimentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListaAtendimentoMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {

            int linhaSelecionada = jListaAtendimento.getSelectedRow();

            Pessoa pessoaSelecionada = this.pessoasPesquisadas.get(linhaSelecionada);

            try {
                ReclamacaoCompleta telaReclamacaoCompleta = new ReclamacaoCompleta();
                telaReclamacaoCompleta.setPessoaEmEdicao(pessoaSelecionada);
                telaReclamacaoCompleta.setVisible(true);
                rodarPesquisa(); //Atuaizar a lista após cadastro/alteraçao
                dispose();
            } catch (Exception ex) {
                Logger.getLogger(PesquisaPessoas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jListaAtendimentoMouseClicked

