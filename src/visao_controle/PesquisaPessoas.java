    public void rodarPesquisa() throws Exception {
       
        revalidate();
        CadastrarPessoaDAO dao = new CadastrarPessoaDAO();

        this.pessoasPesquisadas = dao.pesquisa(jPesquisarPessoa.getText());

        try {

            DefaultTableModel modeloTabela = (DefaultTableModel) jListaAtendimento.getModel();
            jListaAtendimento.getTableHeader().setEnabled(false);
            
            modeloTabela.setNumRows(0); //Limpa a tabela
            if (this.jPesquisarPessoa != null) {
                for (Pessoa p : this.pessoasPesquisadas) {
                    Object[] linha = new Object[5];
                    linha[0] = p.getNome();
                    linha[1] = p.getCpf_cnpj();
                    linha[2] = p.getTelefone();
                    linha[3] = new SimpleDateFormat("dd/MM/yyyy").format(p.getReclamacao().getData_reclamacao());
                    linha[4] = p.getReclamacao().getDescricao();
                    modeloTabela.addRow(linha);

                }
                if (modeloTabela.getRowCount() > 50) {

                    modeloTabela.setNumRows(50);

                }

            }
           

        } catch (Exception ex) {

            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao pesquisar no banco de dados");
        }
    }

