    private void salvarModificacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarModificacaoActionPerformed
        // TODO add your handling code here:
        
        CadastrarPessoaDAO dao = new CadastrarPessoaDAO();
         try {
              if (jNome.getText().isEmpty() == true || jTelefone.getText().isEmpty() == true || jCpf_Cnpj.getText().isEmpty() == true) {
            JOptionPane optionPane = new JOptionPane();
            String msg = "Preencha todos os Campos";
            optionPane.setMessage(msg);
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
            JDialog dialog = optionPane.createDialog(null, "ATENÇÃO");
            dialog.setVisible(true);
        }else{
           PesquisaPessoas telaPesquisa = new PesquisaPessoas();
            pegarInfoTela();
       
             dao.alterarPessoa(pessoaEditada);
             dao.alterarReclamacao(reclamacaoEditada);
             telaPesquisa.setVisible(true);
             String msg = "Alteração realizada";
             JOptionPane optionPane = new JOptionPane();
            optionPane.setMessage(msg);
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
            JDialog dialog = optionPane.createDialog(null, "ATENÇÃO");
            dialog.setVisible(true);
            if(dialog.isShowing()){
            
            telaPesquisa.rodarPesquisa();
            }
             dispose();
         } }catch (Exception ex) {
             Logger.getLogger(ReclamacaoCompleta.class.getName()).log(Level.SEVERE, null, ex);
         }
        
    }//GEN-LAST:event_salvarModificacaoActionPerformed

