        private void escolhaCPFouCNPJ() {

        ButtonGroup radioGroup = new ButtonGroup();
        radioGroup.add(jRadioCelulaCPF);
        radioGroup.add(jRadioCelulaCNPJ);
        try {
            CNPJMask = new MaskFormatter("##.###.###/####-##");
            CPFMask = new MaskFormatter("###.###.###-##");
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
        jRadioCelulaCPF.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                if (e.getStateChange() == ItemEvent.SELECTED) {
                    jCpf_Cnpj.setValue(null);
                    jCpf_Cnpj.setFormatterFactory(new DefaultFormatterFactory(CPFMask));
                }
            }
        });
        jRadioCelulaCNPJ.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                if (e.getStateChange() == ItemEvent.SELECTED) {
                    jCpf_Cnpj.setValue(null);
                    jCpf_Cnpj.setFormatterFactory(new DefaultFormatterFactory(CNPJMask));
                }
            }
        });

    }
    
     private void escolhaTelefone() {

        ButtonGroup radioGroup2 = new ButtonGroup();
        radioGroup2.add(jRadioCelular);
        radioGroup2.add(jRadioFixo);
        try {
            celularMask = new MaskFormatter("(##)#####-####");
            fixoMask = new MaskFormatter("(##)####-####");
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
        jRadioCelular.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                if (e.getStateChange() == ItemEvent.SELECTED) {
                    jTelefone.setValue(null);
                    jTelefone.setFormatterFactory(new DefaultFormatterFactory(celularMask));
                }
                
            }
        });
        jRadioFixo.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                if (e.getStateChange() == ItemEvent.SELECTED) {
                    jTelefone.setValue(null);
                   jTelefone.setFormatterFactory(new DefaultFormatterFactory(fixoMask));
                }
            }
        });

    }
